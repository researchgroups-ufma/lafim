    <path d="M131.42,14.86c0-2.06,1.58-3.5,3.6-3.5s3.55,1.44,3.55,3.5-1.58,3.5-3.55,3.5-3.6-1.39-3.6-3.5ZM132.42,21.58h5.28v24.72h-5.28v-24.72Z"/>
