      // GSAP recalcular o pivô e deslocá-la. svgOrigin fixa o pivô no centro
      // real da gaxeta em coordenadas do SVG, imune ao transform embutido.
      gsap.set(gasket, { svgOrigin: "22.17 24.13" });

